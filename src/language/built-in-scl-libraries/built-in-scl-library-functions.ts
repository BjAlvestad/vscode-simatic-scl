function createGeneralFunction(name: string, inputs?: string[], outputs?: string[], inOuts?: string[], returnType?: string): string {
    return `
    FUNCTION ${name} : ${returnType ?? 'Void'}
    VERSION : 0.1
    ${inputs ?
    `VAR_INPUT
        ${inputs.join(';\n        ')};
    END_VAR
    ` : ''}
    ${outputs ?
    `VAR_OUTPUT
        ${outputs.join(';\n        ')};
    END_VAR
    ` : ''}
    ${inOuts ?
    `VAR_IN_OUT
        ${inOuts.join(';\n        ')};
    END_VAR
    ` : ''}
    BEGIN
    END_FUNCTION
    `.trimStart();
}

